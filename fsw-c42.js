const jumlahLantai = 5
//proses sekali
console.log('Mengambil kain pel')

//proses loop



for(let i = 1; //nilai patokan
    i <= jumlahLantai; //logic untuk mengetahui apakah perulangan bisa dilanjut
    i = i+1// modifier nilai jika sudah menjalankan satu perulangan
){
    
    console.log('Mencuci kain pel di lantai:', i)
    console.log('Memeras kain pel di lantai', i)
    console.log('Mengepel semua bagian lantai:', i)

}

console.log('Ngepel selesai')

